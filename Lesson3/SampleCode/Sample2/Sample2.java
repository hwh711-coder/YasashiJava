class Sample2{
    public static void main(String[] args){
        //変数を宣言する
        int num;
        // 変数への代入
        num = 3;
        System.out.println("変数numの値は"+num+"です。");

        // 変数を変更する
        num = 5;
        System.out.println("変数numの値を変更しました。");
        System.out.println("変数numの新しい値は"+num+"です。");
    }
}